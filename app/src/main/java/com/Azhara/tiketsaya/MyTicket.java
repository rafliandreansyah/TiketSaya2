package com.Azhara.tiketsaya;

public class MyTicket {

    private String date, id_tiket, ketentuan, time_wisata, nama_wisata, lokasi, jumlah_tiket, noWisata;

    public String getNama_wisata() {
        return nama_wisata;
    }
    public String getLokasi() {
        return lokasi;
    }
    public String getJumlah_tiket() {
        return jumlah_tiket;
    }

    public String getDate(){
        return date;
    }

    public String getId_tiket(){
        return id_tiket;
    }

    public String getKetentuan(){
        return ketentuan;
    }

    public String getTime_wisata(){
        return time_wisata;
    }

    public String getNoWisata(){
        return noWisata;
    }


}
